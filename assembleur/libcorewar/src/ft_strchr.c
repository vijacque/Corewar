#include <libcorewar.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != (char)c)
	{
		if (((char)c) != '\0' && s[i] == '\0')
			return (NULL);
		i++;
	}
	return ((char*)s + i);
}
