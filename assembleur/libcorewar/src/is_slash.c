#include <libcorewar.h>

int		is_slash(char *s)
{
	int		i;
	int		ret;

	i = 0;
	ret = 0;
	while (s[i])
	{
		if (s[i] == '/')
			ret = 1;
		i++;
	}
	return (ret);
}
